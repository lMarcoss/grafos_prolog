/*Busca la dirección que se encuentra un punto respecto al origen*/
direccion(X_origen,Y_origen,X_destino,Y_destino,Direccion_destino):- 
	dir1(X_origen,Y_origen,X_destino,Y_destino,Direccion_destino).

dir1(X_origen,Y_origen,X_destino,Y_destino,Direccion_destino):-X_destino < X_origen, dir2(Y_origen,Y_destino,Direccion_destino).
dir1(X_origen,Y_origen,X_destino,Y_destino,Direccion_destino):-X_destino > X_origen, dir3(Y_origen,Y_destino,Direccion_destino).

dir2(Y_origen,Y_destino,Direccion_destino):- Y_destino < Y_origen,Direccion_destino is 1.
dir2(Y_origen,Y_destino,Direccion_destino):- Y_destino > Y_origen,Direccion_destino is 2.

dir3(Y_origen,Y_destino,Direccion_destino):- Y_destino < Y_origen,Direccion_destino is 3.
dir3(Y_origen,Y_destino,Direccion_destino):- Y_destino > Y_origen,Direccion_destino is 4.

/*Busca la dirección de un municipio*/
direccion_municipio(Municipio_origen,Municipio_destino,Direccion):-
  mpio_coords(Municipio_origen,X_origen,Y_origen),
  mpio_coords(Municipio_destino,X_destino,Y_destino),
  direccion(X_origen,Y_origen,X_destino,Y_destino,Direccion).

/*Si la dirección que se encuentra el destino es igual a la del destino, 
toma el vecino como nuevo origen*/
tomar_vecino_origen(Origen,Destino,[Nuevo_origen|Rest],Direccion_destino):-
  direccion_municipio(Origen,Nuevo_origen,Direccion_vecino),
  Direccion_destino == Direccion_vecino,
  write(Nuevo_origen),
  write('\n'),
  buscar_destino(Nuevo_origen,Destino).

tomar_vecino_origen(Origen,Destino,[Nuevo_origen|Rest],Direccion_destino):-
  tomar_vecino_origen(Origen,Destino,Rest,Direccion_destino).



buscar_nuevo_origen(Origen,Destino,Vecinos_origen):-
	member(Destino, Vecinos_origen),
	write(Destino).

buscar_nuevo_origen(Origen,Destino,Vecinos_origen):-
	direccion_municipio(Origen,Destino,Direccion_destino),
  	tomar_vecino_origen(Origen,Destino,Vecinos_origen,Direccion_destino).

/*Obtiene los vecinos de un municipio*/
buscar_vecinos(Municipio,G,Vecinos):-
  neighbors(Municipio,G,Vecinos).
  %write('Vecinos: '),write(Vecinos_origen).

/*Recursividad para recorrer vecinos*/
buscar_destino(Origen,Destino):-
  Origen == Destino,
  write(Origen).

buscar_destino(Origen,Destino):-
	mpio_grafo(Origen,Grafo_municipio),
	buscar_vecinos(Origen,Grafo_municipio,Vecinos_origen),
  	buscar_nuevo_origen(Origen,Destino,Vecinos_origen).

/*Verificar que los municipios existen*/
existe_municipios(Origen,Destino):- 
	mpio_coords(Origen,_,_),
  	mpio_coords(Destino,_,_),
  	buscar_destino(Origen,Destino).	

